#include <iostream>

using namespace std;

int main() {
    int x = 10;
    int y = 15;

    // && Logical and
    // Returns true if both true
    cout << (x>y && x<y);
    
    // || Logical or
    // Returns true either is true
    cout << (x>y || x<y);

    // ! Logical not
    // Returns oposite of the true/false
    cout << (x>y && x<y);

    
    return 0;
}
